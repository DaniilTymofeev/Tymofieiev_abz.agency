import SwiftUI

enum AppFonts {
    static func nunitoRegular(size: CGFloat) -> Font {
        .custom("NunitoSans-Regular", size: size)
    }

    static func nunitoSemiBold(size: CGFloat) -> Font {
        .custom("NunitoSans-SemiBold", size: size)
    }
}
